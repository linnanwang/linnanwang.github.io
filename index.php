<h2>Brief Bio:</h2>
<p>
Shuran Song Ph.D. student at Princeton University, advised by <a href="http://www.cs.princeton.edu/~funk/"> Professor Thomas Funkhouser </a>. 
Before that, she received a BEng. in Computer Science from the Hong Kong University of Science and Technology in 2013. 
Her research interests are in the areas of computer vision. Specifically, she focuses on data-driven scene understanding in 3D.
Shuran was awarded the Wallace Fellowship in 2016, the Facebook Ph.D. Fellowship in 2015-2016.
She was named Siebel Scholar in 2016, and she was the winner of HKUST academic achievement medal in 2013.
</p>
